            <p>
              {categories.map((element) => (
                <div>
                  <Link
                    href={{
                      pathname: "/categories/[category]",
                      query: { category: `${element}` },
                    }}
                  >
                    {element}
                  </Link>
                </div>
              ))}
            </p>
